function Versin() {
    let num = prompt("Digite um numero")
    switch (num) {
        case num=0:
            alert("Seu número é 0")
            break
        case num<0:
            alert("Seu número é menor que 0")
            break
        case num>0:
            alert("Seu número é maior que 0")
        break
    }
}

